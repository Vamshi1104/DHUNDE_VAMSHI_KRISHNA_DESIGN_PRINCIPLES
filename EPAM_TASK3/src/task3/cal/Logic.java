package task3.cal;

public class Logic
{
	//main logic
	float addition(float op1,float op2)
	{
		return op1+op2;
	}
	float subtraction(float op1,float op2)
	{
		return op1-op2;
	}
	float multiplication(float op1,float op2)
	{
		return op1*op2;
	}
	float division(float op1,float op2)
	{
		return op1/op2;
	}
}